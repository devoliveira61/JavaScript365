const primeiroNumero = 5;
const segundoNumero = 5;
const operador = "/";

if (operador === "+") {
  console.log(primeiroNumero + segundoNumero);
} else if (operador === "*") {
  console.log(primeiroNumero * segundoNumero);
} else if (operador === "/") {
  console.log(primeiroNumero / segundoNumero);
} else if (operador === "-") {
  console.log(primeiroNumero - segundoNumero);
}
